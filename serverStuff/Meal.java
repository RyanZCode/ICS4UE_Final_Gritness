public class Meal {
	String name;
	int calories;
	int protein;
	int carbs;
	int fiber;
	int sugar;
	int fat;
	int sodium;
	
	Meal(String name, int calories, int protein, int carbs, int fiber, int sugar, int fat, int sodium) {
		this.name = name;
		this.calories = calories;
		this.protein = protein;
		this.carbs = carbs;
		this.fiber = fiber;
		this.sugar = sugar;
		this.fat = fat;
		this.sodium = sodium;
	}
}
